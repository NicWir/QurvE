            # 1:length(ndx.maxmin)){ pairs.pre[[i]] <- c(minmax[[ndx.maxmin[[i]]]],
            # minmax[[ndx.maxmin[[i]]+1]]) } # extract candidate that is closest to µmax,
            # and respective index of minimum and maximum ndx.maxima_cand <- c()
